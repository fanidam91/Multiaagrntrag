    "# COMMAND ----------\n",
    "import re\n",
    "\n",
    "def execute_spark_sql(sql: str):\n",
    "    \"\"\"Run read-only select commands directly on the Databricks cluster.\"\"\"\n",
    "    clean_sql = sql.strip().lower()\n",
    "    if not clean_sql.startswith(\"select\"):\n",
    "        return {\"error\": \"Only SELECT queries are allowed.\"}\n",
    "    \n",
    "    forbidden = [\"insert\", \"update\", \"delete\", \"drop\", \"alter\", \"create\", \"replace\", \"truncate\"]\n",
    "    for word in forbidden:\n",
    "        if re.search(r'\\b' + word + r'\\b', clean_sql):\n",
    "            return {\"error\": f\"Modifying query keyword '{word}' is not allowed.\"}\n",
    "            \n",
    "    try:\n",
    "        # Execute natively on Spark!\n",
    "        if \"from orders\" in clean_sql:\n",
    "            sql = re.sub(r'\\bfrom orders\\b', 'from adb_core_data_dev_aue.default.orders', sql, flags=re.IGNORECASE)\n",
    "        res_df = spark.sql(sql)\n",
    "        return [row.asDict() for row in res_df.limit(30).collect()]\n",
    "    except Exception as e:\n",
    "        return {\"error\": str(e)}"
