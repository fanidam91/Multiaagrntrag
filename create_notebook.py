    "    if order_id_match:\n",
    "        raw_id = order_id_match.group()\n",
    "        norm_id = raw_id.upper()\n",
    "        print(f\"[Router] Routing to Order Retrieval Agent for Order: {norm_id}\")\n",
    "        details_str = get_order_details(norm_id)\n",
    "        if \"not found\" in details_str:\n",
    "            return f\"I couldn't find a record for order ID **{norm_id}** in our Databricks Delta table.\"\n",
