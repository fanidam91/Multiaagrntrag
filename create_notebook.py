    "        order_id = f\"ORD-{order_id}\"\n",
    "    \n",
    "    rows = execute_spark_sql(f\"SELECT * FROM adb_core_data_dev_aue.default.orders WHERE order_id = '{order_id}'\")\n",
    "    if isinstance(rows, dict) and \"error\" in rows:\n",
    "        return f\"Error: {rows['error']}\"\n",
    "    if not rows:\n",
    "        return f\"Order {order_id} not found.\"\n",
    "    return json.dumps(rows[0], indent=2)\n",
    "\n",
    "def search_orders(query_str: str) -> str:\n",
    "    \"\"\"Search for orders matching customer name or email.\"\"\"\n",
